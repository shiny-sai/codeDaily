public class ListNode {
    int val;
    ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, ListNode next) { this.val = val; this.next = next; }
}
class Solution {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        ListNode p = head;
        int len = 0; // é“¾è¡¨é•¿åº¦
        Map<Integer, ListNode> map = new HashMap(); // éå†è®°å½•é“¾è¡¨é¡ºåº
        // éå†é“¾è¡¨å¾—åˆ°é“¾è¡¨é•¿åº¦
        while (p != null) {
            map.put(len, p);
            len++;
            p = p.next;
        }
        ListNode del = map.get(len - n);
        ListNode prev = map.getOrDefault(len-n-1, null);
        ListNode post = map.getOrDefault(len-n+1, null);
        if (prev == null && post == null) {
            return null;
        } else if (prev != null) {
            prev.next = post;
            del.next = null;
        } else if (prev == null && post != null) {
            return post;
        }
        return head;
    }
}
